# 4. 포맷팅
#포맷팅은 문자열 사이사이에 다른 정보를 삽입하여 조립하는 기법이다.
# %d는 정수, %f는 실수, %s는 문자열, %c 문자 하나, %h 16진수, %o 8진수, %%는 % 문자

#원래는
price = 500
print("궁금하면"+str(price)+"원")
#이나
print("궁금하면",price,"원")
#과 같이 해야하지만, 포맷팅 방식으로 하면

print("궁금하면 %d원" % price)
