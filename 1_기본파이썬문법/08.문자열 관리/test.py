# 2. 문자열 메서드

len(a) # a 문자열의 길이
a.find('o') # a 문자열에서 o가 처음으로 나오는 위치값 
a.rfind('o') # 역순의 문자열 순서에서 o가 처음으로 나오는 위치값
# --> find와 rfind는 찾을 수 없을 때 -1을 리턴한다.
a.index('r')
a.rindex('r')
# --> find와 같이 r의 위치를 찾는 함수이지만, 다른점은 만약 찾지 못하면 -1 리턴이 아니라 예외가 발생하기에 예외처리를 해줘야 한다는 점이다.
a.count('o')
# --> a 문자열에서 o가 나오는 횟수를 카운팅 하는 함수이다.

#등등.. 이외에 함수들은 나중에 알고리즘 풀이에 있어 자주 사용하는 것을 알아두는 것으로 하자.



