

# 1. 문자열 분리
# 파이썬의 문자열에서는 앞에서 셀떄와 뒤에서 셀때 두가지로 출력할 수 있다.
# 0이상인 정수일때는 왼쪽에서부터 순서대로 이지만, 음수인 값일때는 가장 오른쪽 문자열 값부터 역순으로 카운팅한다.

#예를 들어 a = "python"일때, 

a = "python"
print(a[2]) # p가 0 y가 1 t가 2 이므로 t가 출력된다.
print(a[-2]) # n이 -1 o가 -2이므로 o가 출력된다.



for i in range(len(a)): #0부터 a문자열의 길이수 전수 까지 출력. 예를 들어 len(a)가 6이면 0~5까지 출력이다.
    print(a[i], end=',')



#슬라이스
#슬라이스는 문자열에서 하나의 원소가 아니라 문자의 범위를 지정하는 것이다.
# [begin:end:step] 이다.

print(a[2:5]) # 2부터 4번째 문자열 값까지 출력한다. 즉, tho 출력


